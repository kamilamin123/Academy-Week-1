package introtojava;

public class ArithmeticOperations {
	public static void main(String[] args) {
		int var1 = 10;
		int var2  = 20;
		int sum = var1 + var2;
		int sub = var2 - var1 ;
		int multi = var1 * var2;
		int div = var2 / var1 ;
		System.out.println("sum of the variables: " + sum);
		System.out.println("subtraction of the variables: " + sub);
		System.out.println("multiplication of the variables: " + multi);
		System.out.println("division of the variables: " + div);
		
	}
}
